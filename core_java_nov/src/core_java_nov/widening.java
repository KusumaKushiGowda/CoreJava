package core_java_nov;

public class widening 
{
	public static void main(String args[])
	{
		//implicit type casting:
		byte a=89;
		int b=a;
		System.out.println(b);
		
		short x=35;
		int y=x;
		System.out.println(y);
		
		char letter='A';
		int val=letter;
		System.out.println(val);
	}
	

}
